class p16{
	public static void main(String[] args)
	{
		byte a=-80;
		Byte o1 = new Byte(a);
		System.out.println(o1);

		short b=1;
		Short o2 = new Short(b);
		System.out.println(o2);

		int c=10;
		Integer o3 = new Integer(c);
		System.out.println(o3);

		long d=1234567;
		Long o4 = new Long(d);
		System.out.println(o4);

		double e=12.131415;
		Double o5 = new Double(e);
		System.out.println(o5);

		char f='s';
		Character o6= new Character(f);
		System.out.println(o6);

		boolean g=true;
		Boolean o7 = new Boolean(g);
		System.out.println(o7);
	}
}